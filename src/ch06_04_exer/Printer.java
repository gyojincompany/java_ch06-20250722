package ch06_04_exer;

public class Printer {
	public void println(int val) {
		System.out.println(val);
	}
	public void println(Boolean val) {
		System.out.println(val);
	}
	public void println(double val) {
		System.out.println(val);
	}
	public void println(String val) {
		System.out.println(val);
	}
	
}
